document.addEventListener('DOMContentLoaded', () => {
  const cards = document.querySelectorAll('.card');
  const modal = document.getElementById('jobModal');
  const modalTitle = document.getElementById('modal-title');
  const modalBody = document.getElementById('modal-body');
  const closeModal = document.querySelector('.close-modal');
  const jobSearch = document.getElementById('jobSearch');
  const backToTop = document.getElementById('backToTop');

  // Helper: Normalize title for data matching (removes spaces)
  const getNormalizedTitle = (text) => text.replace(/\s+/g, '').trim();

  // Generate Mega Menu Content
  const megaMenuGrid = document.querySelector('.mega-menu-grid');
  if (megaMenuGrid) {
    const categories = [
      { id: 'medical', name: 'medical' },
      { id: 'it', name: 'it' },
      { id: 'arts', name: 'arts' },
      { id: 'edu', name: 'edu' },
      { id: 'biz', name: 'biz' },
      { id: 'safety', name: 'safety' },
      { id: 'science', name: 'science' },
      { id: 'industry', name: 'industry' }
    ];

    categories.forEach(cat => {
      const section = document.getElementById(cat.id);
      const listContainer = megaMenuGrid.querySelector(`ul[data-category="${cat.name}"]`);
      
      if (section && listContainer) {
        const jobTitles = Array.from(section.querySelectorAll('.card h2')).map(h2 => h2.innerText);
        
        jobTitles.forEach(title => {
          const li = document.createElement('li');
          li.innerText = title;
          li.addEventListener('click', (e) => {
            e.stopPropagation();
            const targetCard = Array.from(section.querySelectorAll('.card')).find(c => 
              getNormalizedTitle(c.querySelector('h2').innerText) === getNormalizedTitle(title)
            );
            
            if (targetCard) {
              const offset = 100;
              const bodyRect = document.body.getBoundingClientRect().top;
              const elementRect = targetCard.getBoundingClientRect().top;
              const elementPosition = elementRect - bodyRect;
              const offsetPosition = elementPosition - offset;

              window.scrollTo({ top: offsetPosition, behavior: 'smooth' });
              setTimeout(() => { targetCard.click(); }, 600);
            }
          });
          listContainer.appendChild(li);
        });
      }
    });
  }

  // Modal Open Logic
  const privacyModal = document.getElementById('privacyModal');
  const termsModal = document.getElementById('termsModal');
  const openPrivacy = document.getElementById('openPrivacy');
  const openTerms = document.getElementById('openTerms');

  const openModal = (title) => {
    const originalTitle = title.trim();
    const normalizedTarget = getNormalizedTitle(originalTitle);
    
    // Find data using normalized key
    const dataKey = Object.keys(jobData).find(key => getNormalizedTitle(key) === normalizedTarget);
    const data = dataKey ? jobData[dataKey] : null;

    modalTitle.innerText = originalTitle;
    
    if (data) {
      modalBody.innerHTML = `
        <div class="dept-box">
          <h4 style="color: #2b6cb0; margin-bottom: 10px;"><i class="fas fa-university"></i> 진학 필요 학과</h4>
          <p>${data.dept}</p>
        </div>
        <div class="modal-info-item">
          <h4><i class="fas fa-info-circle"></i> 상세 역할 및 수행 업무</h4>
          <p>${data.desc}</p>
        </div>
        <div class="modal-info-item">
          <h4><i class="fas fa-star"></i> 핵심 직무 역량</h4>
          <p>${data.skills}</p>
        </div>
        <div class="modal-info-item certs-box">
          <h4 style="color: #2f855a;"><i class="fas fa-certificate"></i> 공인 자격증 및 면허</h4>
          <p>${data.certs}</p>
        </div>
        <div class="modal-info-item">
          <h4><i class="fas fa-check-double"></i> 전망 및 유의사항</h4>
          <p>${data.facts}</p>
        </div>
      `;
    } else {
      modalBody.innerHTML = `
        <div class="modal-info-item" style="text-align: center; padding: 40px 0;">
          <i class="fas fa-clock" style="font-size: 3rem; color: #cbd5e0; margin-bottom: 20px; display: block;"></i>
          <p style="font-size: 1.1rem; color: #718096;">해당 직업('${originalTitle}')의 심층 데이터 분석이 진행 중입니다. 곧 업데이트될 예정입니다.</p>
        </div>
      `;
    }

    modal.style.display = 'flex';
    document.body.style.overflow = 'hidden';
  };

  // Additional Modals Logic
  if (openPrivacy) {
    openPrivacy.addEventListener('click', (e) => {
      e.preventDefault();
      privacyModal.style.display = 'flex';
      document.body.style.overflow = 'hidden';
    });
  }
  if (openTerms) {
    openTerms.addEventListener('click', (e) => {
      e.preventDefault();
      termsModal.style.display = 'flex';
      document.body.style.overflow = 'hidden';
    });
  }

  cards.forEach(card => {
    card.addEventListener('click', () => {
      const title = card.querySelector('h2').innerText;
      openModal(title);
    });
  });

  const closeModals = () => {
    modal.style.display = 'none';
    privacyModal.style.display = 'none';
    termsModal.style.display = 'none';
    document.body.style.overflow = 'auto';
  };

  document.querySelectorAll('.close-modal').forEach(btn => {
    btn.addEventListener('click', closeModals);
  });

  window.addEventListener('click', (e) => { 
    if (e.target == modal || e.target == privacyModal || e.target == termsModal) closeModals(); 
  });
  window.addEventListener('keydown', (e) => { if (e.key === 'Escape') closeModals(); });

  // Search & Scroll Logic (remains same)
  if (jobSearch) {
    jobSearch.addEventListener('input', (e) => {
      const term = e.target.value.toLowerCase().trim();
      cards.forEach(card => {
        const jobName = card.querySelector('h2').innerText.toLowerCase();
        card.style.display = jobName.includes(term) ? 'flex' : 'none';
      });
    });
  }

  if (backToTop) {
    window.addEventListener('scroll', () => {
      backToTop.style.display = window.pageYOffset > 300 ? 'flex' : 'none';
    });
    backToTop.addEventListener('click', () => {
      window.scrollTo({ top: 0, behavior: 'smooth' });
    });
  }
});
